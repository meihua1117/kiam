<BR>제 2 조 [용어의 정의]
<BR>이 약관에서 사용하는 용어의 정의는 다음과 같습니다.
<BR>1. 온리원셀링 : 회사의 이름이자 상품의 브랜드명으로 자신의 상품과 서비스를 판매하기 위해 사용하는 솔루션입니다. 
<BR>2. 솔루션구성 : 해당 솔루션에는 아이엠(모바일명함), 콜백문자, 메시지카피솔루션, 문자발송솔루션, 원퍼널솔루션으로 구성된 유료 서비스입니다.
<BR>3. 이용자 : 회사와 서비스 이용계약을 체결하거나 동의한자
<BR>4. 이용신청 : 회사가 정한 별도의 기준과 절차에 따라 서비스 이용을 신청하는 것
<BR>5. 이용계약 : 서비스를 제공 받기 위하여 회사와 이용자 간에 체결하는 계약
<BR>6. 이용정지 : 회사가 정한 일정한 요건에 따라 일정기간 동안 이용자의 서비스 이용을 보류하는 것
<BR>7. 연동해지 : 이용자가 서비스를 일시적으로 사용중지 하는 것
<BR>8. 탈퇴 : 회사와 이용자가 간의 맺은 이용계약을 해약하는 것
<BR>9. 중지아이디 : 이용자가 이용료를 미납하면 익월 서비스를 사용할수 없게 되는 것
<BR>10. 계약종료 : 이용자가 탈퇴하거나 계약해지사유로 계정을 종료하는 경우이며 사업자의 경우 계약종료는 사업자 계약서를 우선으로 함.
<BR>11. 휴면아이디 : 24개월 이상 계속해서 로그인을 포함한 서비스 이용기록이 없는 아이디
<BR>12. 스팸메시지 : 수신자의 의사에 반하여 정보통신망을 통해 일방적으로 전송 또는 게시되는 영리목적의 광고성 정보
<BR>13. 문자 피싱메시지 : 전자금융사기를 목적으로 전송 또는 게시되는 정보
<BR>14. 모바일 : 고객이 사용하고자 등록한 휴대폰 등을 통칭함
<BR>15. 어플리케이션(Application) : 휴대폰에서 온리원셀링 기능이 되도록 만든 프로그램
