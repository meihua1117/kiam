									온리원셀링 이용약관<BR>
제 1 장 총칙
<BR>
<BR>온리원셀링 서비스를 방문해 주셔서 감사합니다.
<BR>온리원셀링은 고객의 휴대폰에 이동 통신사와 약정된 문자발송 조건에 따라 휴대폰과 PC를 연동하는 서비스(웹사이트, 모바일 어플리케이션)로서 사용자가 간편하게 문자를 발송할 수 있도록 서비스를 제공하고 있습니다.
<BR>온리원셀링서비스는 사용자가 하나의 계정으로 PC와 모바일 서비스를 안전하게 이용할 수 있도록 본 약관을
<BR>준비하였습니다. 약관을 읽어주신다면, 더욱 편하게 서비스를 이용하실 수 있으실 것입니다.
<BR>
<BR>제 1 조 [목적]
<BR>이 약관은 온리원연구소("회사")가 제공하는 인터넷에서 운영되는 온리원셀링솔루션(http://kiam.kr이하 "솔루션")의 이용과 관련하여 회사와 이용자의 권리ㆍ의무 및 책임사항, 기타 필요한 사항을 규정함을 목적으로 합니다.
