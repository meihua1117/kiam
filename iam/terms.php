<BR>7. 회사는 스팸메시지ㆍ문자피싱메시지 등으로 인지되는 문자메시지에 대해서 차단을 할 수 있습니다.
<BR>
<BR>제 9 조 [이용자의 의무]
<BR>1. 이용자는 다음 행위를 하여서는 안 됩니다.
<BR> &nbsp; &nbsp; ① 신청 또는 변경 시 허위내용을 등록하는 행위
<BR> &nbsp; &nbsp; ② 타인의 정보를 도용하는 행위
<BR> &nbsp; &nbsp; ③ 다른 이용자의 개인정보를 그 동의 없이 수집, 저장, 공개하는 행위
<BR> &nbsp; &nbsp; ④ 회사가 게시한 정보를 변경하거나 제3자에게 제공하는 행위
<BR> &nbsp; &nbsp; ⑤ 회사와 기타 제3자의 저작권 등 지적재산권에 대한 침해 행위
<BR> &nbsp; &nbsp; ⑥ 회사 및 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위
<BR> &nbsp; &nbsp; ⑦ 외설 또는 폭력적인 메시지, 팩스, 음성, 메일, 기타 공서양속 (공공의 질서와 선량한 풍속)에 반하는 정보를 서비스에 공개 또는 게시하는 행위.
<BR> &nbsp; &nbsp; ⑧ 회사의 동의 없이 영리를 목적으로 서비스를 사용하는 행위.
<BR> &nbsp; &nbsp; ⑨ 타인의 의사에 반하는 내용을 지속적으로 전송하는 행위.
<BR> &nbsp; &nbsp; ⑩ 범죄행위를 목적으로 하거나 범죄행위를 교사하는 행위.
<BR> &nbsp; &nbsp; ⑪ 선량한 풍속 또는 기타 사회질서를 해치는 행위
<BR> &nbsp; &nbsp; ⑫ 현행 법령, 회사가 제공하는 서비스에 정한 약관. 이용안내 및 서비스와 관련하여 공지한 주의사항, 회사가 통지하는 사항, 기타 서비스 이용에 관한 규정을 위반하는 행위
<BR> &nbsp; &nbsp; ⑬ 서비스의 안정적인 운영에 지장을 주거나 줄 우려가 있는 일체의 행위
<BR> &nbsp; &nbsp; ⑭ 제3자에게 임의로 서비스를 임대하는 행위
<BR> &nbsp; &nbsp; ⑮ 기타 불법적이거나 부당한 행위
<BR>2. 회사는 이용자가 제1항의 행위를 하는 경우 이용자의 서비스이용을 정지하고 일방적으로 본 계약을 해지할 수 있습니다.
<BR>3. 이용자는 정보통신망법률의 광고성 정보 전송 시 의무사항 및 회사의 이용약관을 준수하여야 합니다.
<BR>4. 이용자는 스팸메시지ㆍ문자피싱메시지 전송 등 불법행위를 하거나 전기통신사업법 등 관련 법령을 준수하지 않아 발생하는 모든 민ㆍ형사상의 책임을 부담합니다.
<BR>5. 회사는 이용자가 본인 명의가 아닌 타인의 전화번호를 부정하게 사용하는 경우에 서비스의 전부 또는 일부의 이용을 제한할 수 있습니다.
<BR>
<BR>제 4 장 이용자의 권리에 관한 조치
<BR>
<BR>제 10 조 [동의의 철회]
<BR>회사는 이용자가 서비스 화면에서 자신의 개인정보에 대한 수집, 이용 또는 제공에 대한 동의를 철회 할 수 있도록 필요한 조치를 취해야 합니다.
<BR>
<BR>제 11 조 [불만처리]
<BR>1. 회사는 개인정보와 관련하여 이용자의 의견을 수렴하고 불만을 처리하기 위한 절차를 마련하여야 합니다.
<BR>2. 회사는 전화, 전자우편 또는 서비스 화면의 상담창구를 통하여 이용고객의 불만사항을 접수, 처리 하여야 합니다.
