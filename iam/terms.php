<BR>제 5 장 서비스의 이용
<BR>
<BR>제 12 조 [서비스 제공]
<BR>1. 회사는 이용자에게 아래와 같은 서비스를 제공합니다.
<BR> &nbsp; 1) 아이엠 서비스(무유료) : 웹을 이용하여 개인형 명함을 홈페이지 형태로 만들 수 있는 서비스 
<BR> &nbsp; 2) 콜백문자서비스(무료) : 폰에서 온리원앱을 이용하여 전화 수발신 후에 예약문자를 보내는 서비스
<BR> &nbsp; 3) 디비수집서비스(유료) : 포털에 공개된 디비를 수집하여 엑셀로 저장하는 서비스
<BR> &nbsp; 4) 폰문자발송서비스(무유료) : 웹(web)에서 전화번호 등을 업로드하여 앱(Application) 을 이용한 휴대폰 문자메시지 발송 서비스
<BR> &nbsp; 5) 자동예약발송서비스(유료) : 자신의 디비를 이용하여 데일리 발송 예약과 수신동의 고객에게 단계별 예약 메시지를 발송하는 서비스
<BR> &nbsp; 6) 기타서비스 : 기타 회사가 추가 개발하거나 다른 회사와의 제휴계약 등을 통해 이용자에게 제공하는 일체의 서비스
<BR>2. 회사는 서비스를 일정범위로 분할하여 각 범위 별로 이용가능시간을 별도로 지정할 수 있습니다. 다만 이러한 경우에는 그 내용을 사전에 공지합니다. 
<BR>3. 서비스는 연중무휴, 1일 24시간 제공함을 원칙으로 합니다.
<BR>4. 회사는 컴퓨터 등 정보통신설비의 보수점검ㆍ교체 및 고장, 통신두절 또는 운영상의 상당한 이유가 있는 경우 서비스의 제공을 일시적으로 중단할 수 있습니다.
<BR>이 경우 회사는 제9조[이용자에 대한 통지]에 정한 방법으로 이용자에게 통지합니다. 다만, 회사가 사전에 통지할 수 없는 부득이한 사유가 있는 경우 사후에 통지할 수 있습니다.
<BR>5. 회사는 서비스의 제공에 필요한 경우 정기점검을 실시할 수 있으며 정기점검시간은 서비스제공화면에 공지한 바에 따릅니다.
<BR>
<BR>제 13 조 [서비스의 변경]
<BR>1. 회사는 상당한 이유가 있는 경우에 운영상, 기술상의 필요에 따라 제공하고 있는 전부 또는 일부 서비스를 변경할 수 있습니다.
<BR>2. 서비스의 내용, 이용방법, 이용시간에 대하여 변경이 있는 경우에는 변경사유, 변경될 서비스의 내용 및 제공일자 등을 그 변경 전에 해당 서비스 초기화면에 게시하여야 합니다.
<BR>3. 회사는 무료로 제공되는 서비스의 일부 또는 전부를 회사의 정책 및 운영의 필요상 수정, 중단, 변경할 수 있으며 이에 대하여 관련법에 특별한 규정이 없는 한 이용자에게 별도의 보상을 하지 않습니다.
<BR>
<BR>제 14 조 [서비스 이용의 제한 및 정지]
<BR>1. 회사는 이용자가 이 약관의 의무를 위반하거나 서비스의 정상적인 운영을 방해한 경우 서비스 이용을 제한하거나 정지할 수 있습니다.
<BR>2. 회사는 전항에도 불구하고 주민등록법을 위반한 명의도용 및 결제도용, 저작권법을 위반한 불법프로그램의 제공 및 운영방해, 정보통신망법을 위반한 
<BR>스팸메시지 및 불법통신, 해킹, 악성프로그램의 배포, 접속권한 초과행위 등과 같이 관련법을 위반한 경우에는 즉시 영구이용정지를 할 수 있습니다.
<BR>본 항에 따른 서비스 이용정지 시 서비스 내의 금액, 포인트, 혜택 및 권리 등도 모두 소멸되며 회사는 이에 대해 별도로 보상하지 않습니다.
<BR>3. 회사는 이용자가 다음 중 하나에 해당하는 경우 당해 서비스의 이용을 정지 할 수 있습니다.
<BR> &nbsp; &nbsp; ① 방송통신위원회ㆍ한국인터넷진흥원ㆍ미래창조과학부 등 관계기관이 불법스팸메시지ㆍ문자피싱메시지 등 불법행위의 전송사실을 확인하여 이용정지를 요청하는 경우
<BR> &nbsp; &nbsp; ② 이용자가 전송하는 광고로 인하여 회사의 서비스 제공에 장애를 야기하거나 야기할 우려가 있는 경우
<BR> &nbsp; &nbsp; ③ 이용자가 전송하는 광고의 수신자가 스팸메시지로 신고하는 경우
<BR> &nbsp; &nbsp; ④ 이용자에게 제공하는 서비스가 스팸메시지 전송에 이용되고 있는 경우
<BR> &nbsp; &nbsp; ⑤ 제11조[이용자의 의무] 제5항의 정당한 사유에 해당되지 않는 전화번호가 전송되는 경우
<BR>4. 회사는 이용자의 정보가 부당한 목적으로 사용되는 것을 방지하고 보다 원활한 서비스 제공을 위하여 24개월 이상 계속해서 로그인을 포함한 서비스 이용이 없는 아이디를 휴면아이디로 분류하고 서비스 이용을 정지할 수 있습니다.
<BR>5. 회사는 스팸메시지ㆍ문자피싱메시지 전송을 방지하기 위하여 문자의 일일 발송량을 제한 할 수 있으며 자체 모니터링을 강화할 수 있습니다.
<BR>6. 사업자의 경우 사업자계약서가 우선하여 적용됩니다.
<BR>
<BR>제 15 조 [서비스 탈퇴]
<BR>1. 이용자는 이용계약을 해지 하고자 할 때 본인이 직접 서비스를 통하여 신청할 수 있으며 회사는 관련법 등이 정하는 바에 따라 이를 즉시 처리하여야 합니다.
<BR>2. 회사는 이용자가 다음 각 호에 해당할 경우에는 이용자의 동의 없이 이용계약을 해지할 수 있으며 그 사실을 이용자에게 통지합니다.
<BR>다만 회사가 긴급하게 해지할 필요가 있다고 인정하는 경우나 이용자의 귀책사유로 인하여 통지할 수 없는 경우에는 통지를 생략할 수 있습니다.
<BR> &nbsp; &nbsp; ① 이용자가 이 약관을 위반하고 일정 기간 이내에 위반 내용을 해소하지 않는 경우
<BR> &nbsp; &nbsp; ② 회사의 서비스 제공목적 외의 용도로 서비스를 이용하거나 제3자에게 임의로 서비스를 임대한 경우
<BR> &nbsp; &nbsp; ③ 방송통신위원회ㆍ한국인터넷진흥원ㆍ미래창조과학부 등 관계기관이 스팸메시지ㆍ문자피싱메시지 등 불법행위의 전송사실을 확인하여 계약해지를 요청하는 경우
<BR> &nbsp; &nbsp; ④ 제 11조[이용자의 의무] 규정을 위반한 경우
<BR> &nbsp; &nbsp; ⑤ 제 16조[서비스 이용의 제한 및 정지] 규정에 의하여 이용정지를 당한 이후 1년 이내에 이용정지 사유가 재발한 경우
<BR> &nbsp; &nbsp; ⑥ 회사의 이용요금 등의 납입청구에 대하여 이용자가 이용요금을 체납할 경우
<BR>3. 회사는 휴면아이디 상태가 3년 이상 지속될 경우 본 계약을 해지할 수 있으며 해지되기 30일 전 전자우편으로 서비스 계약이 해지 된다는 사실을 통지합니다. 이용자는 통지기간 내에 로그인을 통해 이용계약 해지를 해제할 수 있습니다.
<BR>4. 이용자가 계약을 해지할 경우 관련법 및 개인정보취급방침에 따라 회사가 이용자정보를 보유하는 경우를 제외하고는 해지 즉시 이용자의 모든 데이터는 소멸됩니다.
<BR>
<BR>제 16 조 [문자 발송의 규정]
<BR>1. 회사는 개인 이용자의 아이디 당 문자 전송량을 통신사가 제한하는 건수로 제한할 수 있습니다. 다만, 이용자가 적법한 업무용 광고 발송, 동창회 연락 등 이용자 불편을 최소화하기 위하여 통신사의 사전승인을 얻은 경우 발송량 제한 없이 발송 가능하도록 예외를 인정 할 수 있습니다.
<BR>2. 회사는 이용자가 자신의 폰에 회사에서 제공하는 앱을 설치하고 문자를 발송할수 있습니다. 
<BR>3. 회사는 이용자가 자신의 폰에서 발송하는 발송량을 초과하지 못하도록 시스템에서 발송제한 기능을 지원하지만 해당 기능은 반자동이므로 이용자가 이를 사용하는 과정에서 발송량을 초과하게 될 경우 초과에 대해서는 이용자가 책임을 집니다.
<BR>4. 이용자는 문자를 발송하는 여러기능에서 발송량을 지정할 경우 해당 지침을 수행하지 않아 발송량이 초과된 경우에도 이용자의 책임입니다.
<BR>
<BR>제 17 조 [각종 자료의 저장기간]
<BR>1. 회사는 서비스 별로 이용자가 필요에 의해 저장하고 있는 자료에 대하여 일정한 저장기간을 정할 수 있으며 필요에 따라 그 기간을 변경할 수 있습니다. 
<BR>2. 회사는 이용자가 본사의 시스템에서 탈퇴할 경우 이용자가 남겨둔 주소록, 메시지수발신, 예약발송정보 등 시스템에서 가지고 있는 모든 정보를 5년간 보관하고 해당 기간 중에 이용자가 다시 이용하려고 할 경우 해당 정보를 이용할수 있도록 지원한다. 단, 해당 정보를 복구할 경우 필요한 비용을 청구할 수 있다. 
<BR>
<BR>제 18 조 [게시물의 저작권]
