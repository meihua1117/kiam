<H1>셀링솔루션 이용정책 안내</H1><BR>
<p> 안녕하세요?</p>
<p>셀링솔루션을 이용하시는 분들을 위해서 솔루션 이용에 필요한 <font color="red">1)상담제로화정책, 2)코칭유료화정책을</font> 공지합니다. 결제하기 전에 본 정책을 정확하게 인지하시고 진행하시기바랍니다. 온리원 셀링솔루션은 거의 원가에 가까운 수준으로 공급하기 때문에 이런 정책이 꼭 필요합니다. 감사합니다.</p><br>
<H2>1. 상담제로화 정책 방향</H2>
    <font color="red"><p>아래의 이유때문에 상담제로화  정책을 시행합니다.</p></font>
<H3>1) 공익적 상품가격의 제공</H3>
<p>제4차 산업혁명과 인공지능기술에 의해 자동화가 일반화되었습니다. 갈수록 일자리가 줄고 직원을 두고 일하기 어려운 상황입니다. 이에 온리원셀링에서는 자영업자, 1인기업인, 중소기업인들을 위하여 본 솔루션을 직접 개발하여 기술이용에 대한 로열티를 제거하고, 또한 고객대응에 대한 모든 활동을 제거하여 공익목적의 가격으로 제공할수 있게 되었습니다.</p> 

<H3> 2) 리셀러 수당 제공 전략</H3>
<p>본 상품을 이용하는 분들이 다른 분들을 추천하는 리셀러가 되면 상품가격의 30%를 매월 추천수당으로 제공합니다. 만약 200명을 추천하여 셀링솔루션을 사용하게 되면 매월 120만원씩을 급여처럼 받을 수 있습니다. 흥미로운 사실은 본 솔루션을 쓰는 고객 중에 중간에 탈퇴하지 않는다는 점입니다. 그러니 한번 고객은 영원한 고객이므로 리셀러는 안정적인 수익을 본사와 함께 나누게 됩니다. 그러니 고객들의 솔루션 이용을 위한 상담과 지원 비용을 줄이는 상담제로화 정책을 시행합니다.</p>


<H3>3) 상담료를 제거한 상품가격</H3>
<p>저희 셀링솔루션은 마케팅 전문업체에서 사용하는 솔루션들로서 전체적인 가격은 수천만원에 달합니다. 이런 놀라운 기능을 온리원연구소와 본사가 제휴하여 직접 개발하므로 자영업자, 1인기업인, 중소기업인들의 자동셀링을 지원하기위하여 고객상담관리비용을 제거하므로 월 1만원부터 공급하고 있습니다.</p>

<H3> 4) 세부매뉴얼을 통한 셀프학습 지원전략</H3>
<p>셀링솔루션 고객이 셀프 학습으로 본 솔루션을 사용할 수 있도록 이미지매뉴얼, FAQ자료, 영상자료 등을 제공합니다. 해당 자료를 이용하여 셀프 학습을 하므로 더 효과적인 솔루션 이용역량을 기를 수 있고, 나아가 다른 사람을 지도할수 있는 역량도 함께 개발됩니다. 일반적으로 배우는 것 보다 스스로 연구하면서 배우는 사람이 더 뛰어난 역량을 갖게 됩니다.</p> <br><br>
